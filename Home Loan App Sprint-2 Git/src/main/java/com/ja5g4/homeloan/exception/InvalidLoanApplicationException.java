package com.ja5g4.homeloan.exception;

public class InvalidLoanApplicationException extends Exception {

	public InvalidLoanApplicationException() {
		super();

	}

	public InvalidLoanApplicationException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public InvalidLoanApplicationException(String message, Throwable cause) {
		super(message, cause);

	}

	public InvalidLoanApplicationException(String message) {
		super(message);

	}

	public InvalidLoanApplicationException(Throwable cause) {
		super(cause);

	}

}
