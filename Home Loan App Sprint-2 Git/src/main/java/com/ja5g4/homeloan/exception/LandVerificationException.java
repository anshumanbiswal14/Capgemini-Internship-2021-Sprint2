package com.ja5g4.homeloan.exception;

public class LandVerificationException extends Exception{

	public LandVerificationException() {
		super();
	}

	public LandVerificationException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public LandVerificationException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public LandVerificationException(String message) {
		super(message);
		
	}

	public LandVerificationException(Throwable cause) {
		super(cause);
	
	}
	
	

}
