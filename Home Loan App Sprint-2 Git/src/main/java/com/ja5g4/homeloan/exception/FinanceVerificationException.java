package com.ja5g4.homeloan.exception;

public class FinanceVerificationException extends Exception {

	public FinanceVerificationException() {
		super();

	}

	public FinanceVerificationException(String message, Throwable cause) {
		super(message, cause);

	}

	public FinanceVerificationException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public FinanceVerificationException(String message) {
		super(message);

	}

	public FinanceVerificationException(Throwable cause) {
		super(cause);

	}

}
